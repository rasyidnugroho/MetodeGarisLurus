def MetodeGarisLurus():
    print("MetodeGarisLurus")
    Harga = int(input("Masukan harga : Rp"))
    Residu = int(input("Masukan nilai residu: Rp"))
    UmurEkonomis = int(input("Masukan umur ekonomis(tahun):"))
    Hasil = (Harga-Residu)/UmurEkonomis
    print(Hasil)
MetodeGarisLurus()    